    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        if (!validateField()) {
            return;
        }
        
        int confirmation = JOptionPane.showConfirmDialog(this, "Do you want to change password?");
        if (confirmation != JOptionPane.YES_OPTION) {
            return;
        }
        
        String oldPass = new String(txtOldPass.getPassword());
        String newPass = new String(txtNewPass.getPassword());
        
        UsersDAO userDAO = new UsersDAO();
        boolean result = false;
        try {
            result = userDAO.changePass(id, oldPass, newPass);
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        if (result) {
            JOptionPane.showMessageDialog(this, "Password was changed successfully");
        } else {
            JOptionPane.showMessageDialog(this, "Old password is incorrect");
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

