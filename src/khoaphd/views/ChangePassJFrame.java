    private boolean validateField() {
        String oldPass = new String(txtOldPass.getPassword());
        if (oldPass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Old password is required");
            txtOldPass.requestFocus();
            return false;
        }
        
        String newPass = new String(txtNewPass.getPassword());
        if (newPass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "New password is required");
            txtNewPass.requestFocus();
            return false;
        }
        
        String confirmPass = new String(txtConfirmNew.getPassword());
        if (confirmPass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Confirm new password is required");
            txtConfirmNew.requestFocus();
            return false;
        }
        if (!confirmPass.equals(newPass)) {
            JOptionPane.showMessageDialog(this, "Confirm new password must be the same as new password");
            txtConfirmNew.requestFocus();
            return false;
        }
        
        return true;
    }

